package com.lms.vo;

public class UserAccess {
	private String admin;
	private String pre_analysis;
	private String analysis;
	private String post_analysis;
	public String getAdmin() {
		return admin;
	}
	public void setAdmin(String admin) {
		this.admin = admin;
	}
	public String getPre_analysis() {
		return pre_analysis;
	}
	public void setPre_analysis(String pre_analysis) {
		this.pre_analysis = pre_analysis;
	}
	public String getAnalysis() {
		return analysis;
	}
	public void setAnalysis(String analysis) {
		this.analysis = analysis;
	}
	public String getPost_analysis() {
		return post_analysis;
	}
	public void setPost_analysis(String post_analysis) {
		this.post_analysis = post_analysis;
	}
	

}
